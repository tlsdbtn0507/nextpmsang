// 지지 한글-한자 변환 맵
const BRANCH_HANGUL_TO_HANJA: { [key: string]: string } = {
  '자': '子', '축': '丑', '인': '寅', '묘': '卯', '진': '辰', '사': '巳',
  '오': '午', '미': '未', '신': '申', '유': '酉', '술': '戌', '해': '亥'
};

// 한글 일주를 한자로 변환하는 함수
export const convertHangulToHanja = (hangulDayStem: string): string => {
  if (hangulDayStem.length !== 2) return hangulDayStem;
  
  const stem = hangulDayStem[0];
  const branch = hangulDayStem[1];
  
  const hanjaStem = STEM_HANGUL_TO_HANJA[stem] || stem;
  const hanjaBranch = BRANCH_HANGUL_TO_HANJA[branch] || branch;
  
  return `${hanjaStem}${hanjaBranch}`;
};

